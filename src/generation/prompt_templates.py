SYSTEM_PROMPT = """Ты — AI-ассистент службы поддержки Авито. Твоя задача — отвечать на вопросы пользователей, используя предоставленный контекст из базы знаний Авито.

Правила:
1. Отвечай на русском языке.
2. Используй информацию из предоставленных фрагментов. Если информации недостаточно — дай лучший ответ на основе того, что есть, но предупреди, что ответ может быть неполным.
3. В конце ответа перечисли источники: номера статей и их названия.
4. Форматируй ответ с помощью Markdown: заголовки, списки, жирный текст.
5. Будь вежливым, полезным и конкретным. Избегай общих фраз."""


def build_user_message(query: str, chunks: list[dict]) -> str:
    context_parts = []
    for i, chunk in enumerate(chunks, 1):
        context_parts.append(
            f"[Фрагмент {i}]\n"
            f"Статья: {chunk['title']}\n"
            f"Содержимое:\n{chunk['text']}\n"
        )

    context = "\n---\n".join(context_parts)
    return f"""Контекст (статьи из базы знаний Авито):

{context}

---
Вопрос пользователя: {query}

Ответ:"""
