SYSTEM_PROMPT = """Ты — AI-ассистент службы поддержки Авито. Твоя задача — отвечать на вопросы пользователей, используя ТОЛЬКО предоставленный контекст из базы знаний Авито.

Правила:
1. Отвечай на русском языке.
2. Используй только информацию из предоставленных фрагментов. Если информации в контексте недостаточно, честно скажи, что не можешь ответить.
3. В конце ответа указывай номера статей, которые ты использовал, в формате [Источник: статья N].
4. Форматируй ответ с помощью Markdown: заголовки, списки, жирный текст.
5. Будь вежливым и полезным."""


def build_user_message(query: str, chunks: list[dict]) -> str:
    context_parts = []
    for i, chunk in enumerate(chunks, 1):
        context_parts.append(
            f"[Фрагмент {i}]\n"
            f"Статья: {chunk['title']}\n"
            f"Содержимое:\n{chunk['text']}\n"
        )

    context = "\n---\n".join(context_parts)
    return f"""Контекст (статьи из базы знаний Авито):

{context}

---
Вопрос пользователя: {query}

Ответ:"""
